from pathlib import Path

def process_image(image_path: str, text: str = "") -> str:
    """
    Принимает путь к изображению и необязательный текст.
    Возвращает готовый ответ для пользователя.
    """

    path = Path(image_path)
    if not path.exists():
        return "Ошибка: изображение не найдено."

    #судой пихать обработку текста и изображения

    if text:
        return (
            "Изображение успешно обработано.\n"
            f"Путь к файлу: {image_path}\n"
            f"Подпись: {text}\n"
            "Здесь должен быть итоговый ответ твоей модели."
        )

    return (
        "Изображение успешно обработано.\n"
        f"Путь к файлу: {image_path}\n"
        "Подпись отсутствует.\n"
        "Здесь должен быть итоговый ответ твоей модели."
    )